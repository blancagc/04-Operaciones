public class Operaciones {
	/**
	 * 
	 * @param n1 de tipo entero es donde estara el primer valor agregado
	 * @param n2 de tipo entero
	 * @return regresa la operacion dependiendo de la seleccion
	 */
	public int sumar (int n1, int n2){
		return n1 + n2;
	}
	public int resta (int n1, int n2){
		return n1 - n2;
	}
	public int multiplicacion (int n1, int n2){
		return n1 * n2;
	}
	/**
	 * 
	 * @param n1 tipo float
	 * @param n2 tipo float
	 * @return la operación que se va a realizar en este caso división
	 */
	public float division (float n1, float n2){
		return n1 / n2;
	}

}
